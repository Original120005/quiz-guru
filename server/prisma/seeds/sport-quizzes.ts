async function seedSportQuizzes() {
  console.log('Начинаем добавление квизов по спорту...');

  for (const quizData of sportQuizzes) {
    try {
      await prisma.quiz.create({
        data: {
          ...quizData,
          categoryId: 4 // Игры
        }
      });
      console.log(`✓ Добавлен квиз: "${quizData.title}"`);
    } catch (error) {
      console.log(`✗ Ошибка с квизом "${quizData.title}":`, error);
    }
  }
  
  console.log('Готово! Добавлены квизы по спорту');
}

seedSportQuizzes()
  .catch(console.error)
  .finally(() => prisma.$disconnect());